import { readFile, readdir, writeFile } from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"
import prettier from "prettier"
import ts from "typescript"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, "..", "..", "..")
const componentsDir = path.join(repoRoot, "packages", "remix", "src", "components")
const outputPath = path.join(__dirname, "component-api-generated.js")
const checkMode = process.argv.includes("--check")

const componentFiles = (await readdir(componentsDir)).filter((file) => file.endsWith(".tsx")).sort()
const componentApiSections = {}
const componentSourceFiles = new Map()

for (const fileName of componentFiles) {
  const sourcePath = path.join(componentsDir, fileName)
  const sourceText = await readFile(sourcePath, "utf8")
  const sourceFile = ts.createSourceFile(sourcePath, sourceText, ts.ScriptTarget.Latest, true, ts.ScriptKind.TSX)
  componentSourceFiles.set(sourcePath, sourceFile)
}

for (const fileName of componentFiles) {
  const componentName = fileName.replace(/\.tsx$/, "")
  const sourcePath = path.join(componentsDir, fileName)
  const sourceFile = componentSourceFiles.get(sourcePath)
  if (!sourceFile) continue

  const generated = buildApiSection(componentName, sourceFile, sourcePath, componentSourceFiles)
  if (!generated) continue

  componentApiSections[componentName.toLowerCase()] = generated.section
  for (const aliasName of generated.aliasNames) {
    if (!componentApiSections[aliasName]) {
      componentApiSections[aliasName] = generated.section
    }
  }
}

const generatedSource = `${header()}\n\nexport const componentApiSections = ${JSON.stringify(componentApiSections, null, 2)}\n`
const prettierConfig = (await prettier.resolveConfig(outputPath)) ?? {}
const formattedGeneratedSource = await prettier.format(generatedSource, {
  ...prettierConfig,
  filepath: outputPath,
})

if (checkMode) {
  const currentSource = await readCurrentOutput()
  if (currentSource !== formattedGeneratedSource) {
    throw new Error("component-api-generated.js is stale. Run `pnpm --filter @lukasmurdock/docs run api:build`.")
  }
  console.log(`Component API docs are up to date for ${Object.keys(componentApiSections).length} components.`)
} else {
  await writeFile(outputPath, formattedGeneratedSource)
  console.log(`Generated API docs for ${Object.keys(componentApiSections).length} components.`)
}

async function readCurrentOutput() {
  try {
    return await readFile(outputPath, "utf8")
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return ""
    }
    throw error
  }
}

function header() {
  return [
    "// AUTO-GENERATED FILE. DO NOT EDIT.",
    "//",
    "// Generated by: apps/docs/src/generate-component-api-docs.mjs",
    "// Run: pnpm --filter @lukasmurdock/docs run api:build",
  ].join("\n")
}

function buildApiSection(componentName, sourceFile, sourcePath, componentSourceFiles) {
  const localDeclarations = getTypeDeclarations(sourceFile)
  const importedDeclarations = getImportedTypeDeclarations(sourceFile, sourcePath, componentSourceFiles)
  const resolvableDeclarations = new Map(importedDeclarations)
  for (const [name, declaration] of localDeclarations) {
    resolvableDeclarations.set(name, declaration)
  }

  const exportedDeclarations = getExportedTypeDeclarations(sourceFile)
  if (exportedDeclarations.size === 0) return null

  const rootNames = resolveRootTypeNames(componentName, sourceFile, exportedDeclarations)
  if (rootNames.length === 0) return null

  const includedNames = resolveIncludedTypeNames(rootNames, sourceFile, localDeclarations)
  if (includedNames.length === 0) return null

  const declarations = includedNames
    .map((name) => localDeclarations.get(name))
    .filter(Boolean)
    .map((declaration) => declaration.getText(sourceFile).trim())
    .join("\n\n")

  const propsTables = rootNames
    .map((name) => buildPropsTable(name, exportedDeclarations.get(name), sourceFile, resolvableDeclarations))
    .filter(Boolean)

  const sectionLines = ["## API", "", "Type definitions are generated from component source.", ""]

  if (propsTables.length > 0) {
    sectionLines.push(propsTables.join("\n\n"), "")
  }

  sectionLines.push("### Type definitions", "", "```ts", declarations, "```")

  const section = sectionLines.join("\n")

  const aliasNames = rootNames
    .map((name) => name.replace(/Props$/, "").toLowerCase())
    .filter((name) => name !== componentName.toLowerCase())

  return { section, aliasNames }
}

function buildPropsTable(typeName, declaration, sourceFile, allDeclarations) {
  const rows = extractPropRows(declaration, sourceFile, allDeclarations)
  if (rows.length === 0) return null

  const lines = [`### \`${typeName}\``, "", "| Prop | Type | Required | Default |", "| --- | --- | --- | --- |"]

  for (const row of rows) {
    const propCell = `\`${escapeMarkdownCell(row.name)}\``
    const typeCell = `\`${escapeMarkdownCell(row.type)}\``
    const requiredCell = row.required ? "yes" : "no"
    const defaultCell = row.defaultValue ? `\`${escapeMarkdownCell(row.defaultValue)}\`` : "-"
    lines.push(`| ${propCell} | ${typeCell} | ${requiredCell} | ${defaultCell} |`)
  }

  return lines.join("\n")
}

function extractPropRows(declaration, sourceFile, allDeclarations) {
  if (!declaration) return []

  let members = []
  if (ts.isTypeAliasDeclaration(declaration)) {
    members = collectTypeMembers(declaration.type, sourceFile, allDeclarations)
  } else if (ts.isInterfaceDeclaration(declaration)) {
    members = collectInterfaceMembers(declaration, sourceFile, allDeclarations)
  }

  const seenNames = new Set()

  return members
    .filter((member) => ts.isPropertySignature(member) && !!member.name)
    .map((member) => {
      const memberSourceFile = member.getSourceFile()
      const name = getPropertyNameText(member.name, memberSourceFile)
      if (seenNames.has(name)) return null
      seenNames.add(name)

      const type = member.type ? member.type.getText(memberSourceFile).trim() : "unknown"
      const defaultValue = getJSDocDefault(member)

      return {
        name,
        type,
        required: !member.questionToken,
        defaultValue,
      }
    })
    .filter(Boolean)
}

function collectInterfaceMembers(interfaceDeclaration, sourceFile, allDeclarations, seenTypeNames = new Set()) {
  const members = [...interfaceDeclaration.members]

  for (const heritageClause of interfaceDeclaration.heritageClauses ?? []) {
    for (const heritageType of heritageClause.types ?? []) {
      members.push(...collectTypeMembers(heritageType, sourceFile, allDeclarations, seenTypeNames))
    }
  }

  return members
}

function collectTypeMembers(typeNode, sourceFile, allDeclarations, seenTypeNames = new Set()) {
  if (ts.isTypeLiteralNode(typeNode)) return [...typeNode.members]
  if (ts.isParenthesizedTypeNode(typeNode)) {
    return collectTypeMembers(typeNode.type, sourceFile, allDeclarations, seenTypeNames)
  }
  if (ts.isIntersectionTypeNode(typeNode) || ts.isUnionTypeNode(typeNode)) {
    return typeNode.types.flatMap((type) => collectTypeMembers(type, sourceFile, allDeclarations, seenTypeNames))
  }

  if (ts.isTypeReferenceNode(typeNode)) {
    return collectTypeReferenceMembers(typeNode, sourceFile, allDeclarations, seenTypeNames)
  }

  return []
}

function collectTypeReferenceMembers(typeReference, sourceFile, allDeclarations, seenTypeNames) {
  const typeName = getTypeReferenceName(typeReference)
  if (!typeName) return []

  if (typeName === "Omit") {
    return collectOmitMembers(typeReference, sourceFile, allDeclarations, seenTypeNames)
  }

  if (typeName === "Pick") {
    return collectPickMembers(typeReference, sourceFile, allDeclarations, seenTypeNames)
  }

  const declaration = allDeclarations.get(typeName)
  if (!declaration || seenTypeNames.has(typeName)) return []

  const nextSeen = new Set(seenTypeNames)
  nextSeen.add(typeName)

  if (ts.isTypeAliasDeclaration(declaration)) {
    return collectTypeMembers(declaration.type, sourceFile, allDeclarations, nextSeen)
  }

  if (ts.isInterfaceDeclaration(declaration)) {
    return collectInterfaceMembers(declaration, sourceFile, allDeclarations, nextSeen)
  }

  return []
}

function collectOmitMembers(typeReference, sourceFile, allDeclarations, seenTypeNames) {
  const baseType = typeReference.typeArguments?.[0]
  const omittedKeysNode = typeReference.typeArguments?.[1]
  if (!baseType) return []

  const members = collectTypeMembers(baseType, sourceFile, allDeclarations, seenTypeNames)
  const omittedKeys = collectLiteralTypeKeys(omittedKeysNode)
  if (omittedKeys.size === 0) return members

  return members.filter((member) => {
    if (!ts.isPropertySignature(member) || !member.name) return true
    return !omittedKeys.has(getPropertyNameText(member.name, member.getSourceFile()))
  })
}

function collectPickMembers(typeReference, sourceFile, allDeclarations, seenTypeNames) {
  const baseType = typeReference.typeArguments?.[0]
  const pickedKeysNode = typeReference.typeArguments?.[1]
  if (!baseType) return []

  const members = collectTypeMembers(baseType, sourceFile, allDeclarations, seenTypeNames)
  const pickedKeys = collectLiteralTypeKeys(pickedKeysNode)
  if (pickedKeys.size === 0) return members

  return members.filter((member) => {
    if (!ts.isPropertySignature(member) || !member.name) return false
    return pickedKeys.has(getPropertyNameText(member.name, member.getSourceFile()))
  })
}

function collectLiteralTypeKeys(typeNode) {
  const keys = new Set()
  if (!typeNode) return keys

  const visit = (node) => {
    if (ts.isParenthesizedTypeNode(node)) {
      visit(node.type)
      return
    }

    if (ts.isUnionTypeNode(node)) {
      for (const unionNode of node.types) visit(unionNode)
      return
    }

    if (!ts.isLiteralTypeNode(node)) return

    const literal = node.literal
    if (ts.isStringLiteral(literal) || ts.isNumericLiteral(literal)) {
      keys.add(literal.text)
    }
  }

  visit(typeNode)
  return keys
}

function getTypeReferenceName(typeReference) {
  const typeName = typeReference.typeName
  if (ts.isIdentifier(typeName)) return typeName.text
  if (ts.isQualifiedName(typeName)) return typeName.right.text
  return undefined
}

function getPropertyNameText(nameNode, sourceFile) {
  if (ts.isIdentifier(nameNode) || ts.isStringLiteral(nameNode) || ts.isNumericLiteral(nameNode)) {
    return nameNode.text
  }

  return nameNode.getText(sourceFile).trim()
}

function getJSDocDefault(node) {
  const defaultTag = ts.getJSDocTags(node).find((tag) => tag.tagName.text === "default")
  if (!defaultTag) return undefined

  const text = normalizeJSDocCommentText(defaultTag.comment)
  return text || undefined
}

function normalizeJSDocCommentText(comment) {
  if (!comment) return ""
  if (typeof comment === "string") return comment.trim()
  if (Array.isArray(comment)) {
    return comment
      .map((part) => {
        if (typeof part === "string") return part
        if (part && typeof part === "object" && "text" in part) return part.text
        return ""
      })
      .join("")
      .trim()
  }
  return String(comment).trim()
}

function escapeMarkdownCell(value) {
  return String(value).replace(/\|/g, "\\|").replace(/\n+/g, " ").trim()
}

function resolveRootTypeNames(componentName, sourceFile, exportedDeclarations) {
  const exactPropsName = `${componentName}Props`
  const roots = []

  if (exportedDeclarations.has(exactPropsName)) {
    roots.push(exactPropsName)
  }

  for (const name of exportedDeclarations.keys()) {
    if (name.endsWith("Props") && !roots.includes(name)) roots.push(name)
  }

  return roots.sort((a, b) => byDeclarationPosition(exportedDeclarations, sourceFile, a, b))
}

function resolveIncludedTypeNames(rootNames, sourceFile, allDeclarations) {
  const included = new Set()
  const queue = [...rootNames]

  while (queue.length > 0) {
    const name = queue.shift()
    if (!name || included.has(name)) continue

    const declaration = allDeclarations.get(name)
    if (!declaration) continue

    included.add(name)

    for (const referencedType of collectReferencedTypeNames(declaration)) {
      if (!included.has(referencedType) && allDeclarations.has(referencedType)) {
        queue.push(referencedType)
      }
    }
  }

  const rootSet = new Set(rootNames)
  const orderedRoots = rootNames.filter((name) => included.has(name))
  const orderedReferenced = [...included]
    .filter((name) => !rootSet.has(name))
    .sort((a, b) => byDeclarationPosition(allDeclarations, sourceFile, a, b))

  return [...orderedRoots, ...orderedReferenced]
}

function byDeclarationPosition(exportedDeclarations, sourceFile, left, right) {
  const leftDeclaration = exportedDeclarations.get(left)
  const rightDeclaration = exportedDeclarations.get(right)
  const leftPos = leftDeclaration ? leftDeclaration.getStart(sourceFile) : Number.MAX_SAFE_INTEGER
  const rightPos = rightDeclaration ? rightDeclaration.getStart(sourceFile) : Number.MAX_SAFE_INTEGER
  return leftPos - rightPos
}

function getTypeDeclarations(sourceFile) {
  const declarations = new Map()

  for (const statement of sourceFile.statements) {
    if (ts.isTypeAliasDeclaration(statement) || ts.isInterfaceDeclaration(statement)) {
      declarations.set(statement.name.text, statement)
    }
  }

  return declarations
}

function getImportedTypeDeclarations(sourceFile, sourcePath, componentSourceFiles) {
  const importedDeclarations = new Map()

  for (const statement of sourceFile.statements) {
    if (!ts.isImportDeclaration(statement)) continue

    const importClause = statement.importClause
    if (!importClause || !importClause.namedBindings || !ts.isNamedImports(importClause.namedBindings)) {
      continue
    }

    const moduleSpecifier = statement.moduleSpecifier
    if (!ts.isStringLiteral(moduleSpecifier)) continue

    const importedSourcePath = resolveImportedSourcePath(sourcePath, moduleSpecifier.text, componentSourceFiles)
    if (!importedSourcePath) continue

    const importedSourceFile = componentSourceFiles.get(importedSourcePath)
    if (!importedSourceFile) continue

    const importedTypeDeclarations = getTypeDeclarations(importedSourceFile)

    for (const element of importClause.namedBindings.elements) {
      const importedName = element.propertyName?.text ?? element.name.text
      const localName = element.name.text
      const declaration = importedTypeDeclarations.get(importedName)
      if (declaration) importedDeclarations.set(localName, declaration)
    }
  }

  return importedDeclarations
}

function resolveImportedSourcePath(sourcePath, importPath, componentSourceFiles) {
  if (!importPath.startsWith(".")) return null

  const parentDir = path.dirname(sourcePath)
  const candidates = [
    path.resolve(parentDir, `${importPath}.tsx`),
    path.resolve(parentDir, `${importPath}.ts`),
    path.resolve(parentDir, importPath, "index.tsx"),
    path.resolve(parentDir, importPath, "index.ts"),
  ]

  return candidates.find((candidate) => componentSourceFiles.has(candidate)) ?? null
}

function getExportedTypeDeclarations(sourceFile) {
  const declarations = new Map()

  for (const statement of sourceFile.statements) {
    if (!hasExportModifier(statement)) continue

    if (ts.isTypeAliasDeclaration(statement) || ts.isInterfaceDeclaration(statement)) {
      declarations.set(statement.name.text, statement)
    }
  }

  return declarations
}

function hasExportModifier(statement) {
  return !!statement.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword)
}

function collectReferencedTypeNames(node) {
  const names = new Set()

  const visit = (currentNode) => {
    if (ts.isTypeReferenceNode(currentNode)) {
      const typeName = currentNode.typeName
      if (ts.isIdentifier(typeName)) {
        names.add(typeName.text)
      } else if (ts.isQualifiedName(typeName)) {
        names.add(typeName.right.text)
      }
    }

    ts.forEachChild(currentNode, visit)
  }

  visit(node)
  return names
}
